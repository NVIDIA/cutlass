executor = Executor()


def loop_selector(
    start: Any,
    stop: Any,
    step: Any,
    *,
    write_args: list[Any] = [],
    full_write_args_count: int = 0,
    write_args_names: list[str] = [],
    unroll: int = -1,
    unroll_full: bool = False,
    prefetch_stages: int | None = None,
    vectorize: int | None = None,
    at_least_once: bool = False,
) -> Callable[..., Any]:
    log().debug(
        "start [%s] stop [%s] step [%s] write_args [%s] full_write_args_count [%s] write_args_names [%s] unroll [%s] unroll_full [%s] prefetch_stages [%s] vectorize [%s] at_least_once [%s]",
        start,
        stop,
        step,
        write_args,
        full_write_args_count,
        write_args_names,
        unroll,
        unroll_full,
        prefetch_stages,
        vectorize,
        at_least_once,
    )
    from .typing import Integer, Numeric

    def _maybe_upcast(value: Any) -> Any:
        if isinstance(value, Integer):
            value = value.ir_value()

        return value

    start = _maybe_upcast(start)
    stop = _maybe_upcast(stop)
    step = _maybe_upcast(step)

    def ir_loop(func: Callable[..., Any]) -> Any:
        return executor.for_execute(
            func,
            start,
            stop,
            step,
            write_args=write_args,
            full_write_args_count=full_write_args_count,
            write_args_names=write_args_names,
            unroll=unroll,
            unroll_full=unroll_full,
            prefetch_stages=prefetch_stages,
            vectorize=vectorize,
            at_least_once=at_least_once,
        )

    return ir_loop


def if_selector(pred: Any, write_args: list[Any] = []) -> Callable[..., Any]:
    log().debug("pred [%s] write_args [%s]", pred, write_args)
    # Handle Numeric types here?

    from .typing import Numeric

    if isinstance(pred, Numeric):
        pred = pred.value

    def ir_loop(func: Callable[..., Any]) -> Any:
        return func(pred, *write_args)

    return ir_loop


def while_selector(*, write_args: list[Any] = []) -> Callable[..., Any]:
    def ir_while_loop(func: Callable[..., Any]) -> Any:
        return func(*write_args)

    return ir_while_loop


def while_executor(
    while_before_block: Callable[..., Any],
    while_after_block: Callable[..., Any],
    write_args: list[Any] = [],
    full_write_args_count: int = 0,
    write_args_names: list[str] = [],
) -> Any:
    return executor.while_execute(
        while_before_block,
        while_after_block,
        write_args,
        full_write_args_count,
        write_args_names,
    )


def if_executor(
    pred: Any,
    then_block: Callable[..., Any],
    else_block: Callable[..., Any] | None = None,
    write_args: list[Any] = [],
    full_write_args_count: int = 0,
    write_args_names: list[str] = [],
) -> Any:
    return executor.if_execute(
        pred,
        then_block,
        else_block,
        write_args,
        full_write_args_count,
        write_args_names,
    )


def ifExp_executor(
    *,
    pred: Any,
    block_args: tuple[Any, ...],
    then_block: Callable[..., Any],
    else_block: Callable[..., Any],
) -> Any:
    if not executor._is_dynamic_expression(pred):  # type: ignore[misc]
        return then_block(*block_args) if pred else else_block(*block_args)
    else:
        return executor.ifexp_execute(pred, block_args, then_block, else_block)


