class DSLOptimizationWarning(Warning):
    """
    This warning is used to warn the user about the optimization related issues in DSL.
    """

    def __init__(self, message: str) -> None:
        self.message = message
        super().__init__()

    def __str__(self) -> str:
        return self.message


def range_value_check(*args: Any) -> tuple[int, int, int]:
    """
    Ensure all `range_constexpr` bounds are compile-time constants (Python ints).
    """
    try:
        args = tuple(arg.__index__() for arg in args)

        # Compute range size and warn if it's too large
        start = 0
        end = 0
        step = 1
        if len(args) == 1:
            end = args[0]
        elif len(args) == 2:
            start = args[0]
            end = args[1]
        elif len(args) == 3:
            start = args[0]
            end = args[1]
            step = args[2]

        range_length = (abs(end - start) - 1) // abs(step) + 1
        if range_length >= 64:
            warnings.warn(
                f"This static loop has {range_length} iterations, which may be very slow to compile, consider using `cutlass.range(..., unroll_full=True)` instead.",
                category=DSLOptimizationWarning,
                stacklevel=2,
            )

        return (start, end, step)
    except:
        raise DSLRuntimeError(
            "`range_constexpr` requires constexpr (compile-time constant) for all arguments.",
            suggestion="Use `range` instead of `range_constexpr`.",
        )


@lru_cache(maxsize=1)
def _get_self_module() -> types.ModuleType | None:
    """
    This function is used to get the owning module of this function.
    """
    return inspect.getmodule(_get_self_module)


@lru_cache(maxsize=16)
def cf_symbol_check(symbol: Any) -> None:
    """
    Check if the symbol is control flow symbol from current module.
    """

    failed = False
    name = symbol.__name__
    self_module = _get_self_module()
    if inspect.ismodule(symbol):
        if not self_module.__name__.startswith(name):  # type: ignore[union-attr]
            failed = True
    else:
        owning_module = inspect.getmodule(symbol)
        root_module = owning_module.__name__.split(".")[0]  # type: ignore[union-attr]
        self_root_module = self_module.__name__.split(".")[0]  # type: ignore[union-attr]
        if root_module != self_root_module:
            failed = True

    if failed:
        raise DSLRuntimeError(
            f"Incorrect `{name}` is used.",
            suggestion=f"Please avoid overriding `{name}` from DSL package.",
        )


def redirect_builtin_function(fcn: Any) -> Any:
    """
    This function is used to redirect built-in function call
    to the function defined in DSL package.
    """
    # Only redirect if it's a built-in
    if fcn is builtins.bool:
        return bool_cast

    if isinstance(fcn, BuiltinFunctionType):
        if fcn in [builtins.exec, builtins.eval]:
            raise DSLRuntimeError(
                f"Built-in function `{fcn.__name__}` is not supported in DSL.",
            )
        if executor._builtin_redirector:
            return executor._builtin_redirector(fcn)
    return fcn


def copy_members(dest: object, src: object) -> None:
    """
    Copies all non-callable, non-dunder members from src to dest if they exist in src.
    Skips members that are callables or have names starting with double underscores.
    """
    if id(dest) == id(src):
        return

    members = getmembers(dest)
    for name, value in members:
        if (
            name.startswith("__")
            or isinstance(value, Callable)  # type: ignore[arg-type]
            or not hasattr(src, name)
        ):
            continue
        try:
            setattr(dest, name, getattr(src, name))
        except AttributeError:
            # AttributeError means the member is no settable, like property without setter, just ignore
            pass


def get_locals_or_none(locals: dict[str, Any], symbols: list[str]) -> list[Any]:
    """
    Given a locals() dictionary and a list of symbol names, return a list of their values
    in the same order as the symbols list. If a symbol is not present in locals, None is returned
    for that symbol.
    """
    variables = []
    for symbol in symbols:
        if symbol in locals:
            variables.append(locals[symbol])
        else:
            variables.append(None)
    return variables


def closure_check(
    closures: list[Any], _visited: set[tuple[str, int]] | None = None
) -> None:
    """
    Check if the closures have any unsupported capture
    """
    if _visited is None:
        _visited = set()

    for closure in closures:
        # Use (function name, id) as identity to skip already-processed closures
        # and prevent infinite recursion with mutually-recursive captured functions
        closure_identity = (closure.__name__, id(closure))
        if closure_identity in _visited:
            continue
        _visited.add(closure_identity)

        closure_vars = inspect.getclosurevars(closure)
        for name, value in closure_vars.nonlocals.items():
            if inspect.ismodule(value):
                continue
            if inspect.isfunction(value) or inspect.ismethod(value):
                closure_check([value], _visited)
                continue
            raise DSLRuntimeError(
                f"Function `{closure.__name__}` is a closure that captures variable `{name}` and is not supported in dynamic control flow",
                suggestion="Please explicitly pass in captured variables as arguments",
            )


@dataclass
class FormattedValue:
    """
    Represents a value (from an f-string interpolation) along with its conversion and format specification.

    This is a runtime representation of ast.FormattedValue.
    See https://docs.python.org/3/library/ast.html#ast.FormattedValue

    This class is used to encapsulate a value that is to be embedded into an f-string formatted string,
    along with any conversion flags (such as '!s', '!r', or '!a') and a list of format specification strings
    (e.g., '.2f', '05d', etc).

    Attributes:
        value (Any): The value to be formatted.
        conversion (int): The ASCII code of the conversion character, defaulting to -1 (no conversion).
        format_spec (Optional[List[str]]): List of format specifier strings.
    """

    value: Any
    conversion: int = -1
    format_spec: list[str] | None = None

    def to_str(self) -> tuple[str, Any | None]:
        """
        Converts the FormattedValue into a format string component and (optional) dynamic argument.

        If the value is a dynamic expression (requires runtime evaluation), returns a printf-style placeholder and
        the corresponding dynamic value for later substitution. For static (non-dynamic) values, returns the fully
        rendered literal string and None for the dynamic value.

        Returns:
            Tuple[str, Optional[Any]]: A tuple where the first element is the format string component and the second
            element is the dynamic value (or None if not required).

        Raises:
            DSLRuntimeError: If the format spec is invalid or not supported for dynamic expressions.
        """
        if executor._is_dynamic_expression(self.value):  # type: ignore[misc]
            if self.conversion != -1:
                warnings.warn(
                    "Conversion may not be honored for dynamic expressions",
                    stacklevel=3,
                )
            if self.format_spec is not None and len(self.format_spec) > 1:
                raise DSLRuntimeError(
                    "At most one format specifier is supported for dynamic expressions",
                )
            if self.format_spec is not None:
                if self.format_spec[0].startswith(("<", ">", "^")):
                    raise DSLRuntimeError(
                        "Alignment specifier is not supported for dynamic expressions",
                    )
                return f"%{self.format_spec[0]}", self.value
            return "{}", self.value
        else:
            conversion_str = ""
            if self.conversion == 97:
                conversion_str = "!a"  # ASCII
            elif self.conversion == 114:
                conversion_str = "!r"  # repr
            elif self.conversion == 115:
                conversion_str = "!s"  # str
            if self.format_spec is not None:
                f_str = f"{{{conversion_str}:{self.format_spec[0]}}}"
            else:
                f_str = f"{{{conversion_str}}}"
            return f_str.format(self.value), None


def fstring_decompose(
    joinedStrComponent: list[str | FormattedValue],
) -> tuple[Any, ...]:
    """
    Decomposes a joined f-string component list into a format string and dynamic arguments.

    This function takes a sequence of components representing parts of an f-string,
    which may include both literal strings and FormatValue instances.
    It assembles a format string with placeholders for dynamic values
    and collects the actual dynamic argument values in order.

    Args:
        joinedStrComponent (List[Union[str, FormattedValue]]): The components of the f-string.

    Returns:
        Tuple[str, ...]: A tuple containing the format string as the first element,
                         followed by all dynamic argument values.

    Raises:
        DSLAstPreprocessorError: If an unsupported component type is encountered.
    """
    format_string = ""
    dynamic_args = []
    for component in joinedStrComponent:
        if isinstance(component, FormattedValue):
            s, v = component.to_str()
            format_string += s
            if v is not None:
                # This is a dynamic value, add to dynamic_args
                dynamic_args.append(v)
        elif isinstance(component, str):
            format_string += component
        else:
            raise DSLAstPreprocessorError(
                f"Unsupported component type in f-string: {type(component)}",
            )
    return (format_string, *dynamic_args)
