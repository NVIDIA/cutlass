class Executor:
    """
    The Executor class handles dynamic and compile-time (constexpr) execution
    of "for" loops and "if-else-elif" statements.

    Methods:
        set_functions:  Assigns the functions for checking loop bounds and
                        conditional evaluation.

        for_execute: Generates MLIR for OP
        while_execute: Generates MLIR while OP
        if_execute: generate MLIR if OP
    """

    def __init__(self) -> None:
        self._is_dynamic_expression: Callable[..., Any] | None = None
        self._loop_execute_range_dynamic: Callable[..., Any] | None = None
        self._if_dynamic: Callable[..., Any] | None = None
        self._while_dynamic: Callable[..., Any] | None = None
        self._compare_executor: Callable[..., Any] | None = None
        self._builtin_redirector: Callable[..., Any] | None = None
        self._ifexp_dynamic: Callable[..., Any] | None = None

    @staticmethod
    def _default_builtin_redirector(fcn: Callable[..., Any]) -> Callable[..., Any]:
        # Default to no redirect
        return fcn

    def set_functions(
        self,
        *,
        is_dynamic_expression: Callable[..., Any],
        loop_execute_range_dynamic: Callable[..., Any],
        if_dynamic: Callable[..., Any],
        while_dynamic: Callable[..., Any],
        compare_executor: Callable[..., Any],
        builtin_redirector: Callable[..., Any] = _default_builtin_redirector,
        ifexp_dynamic: Callable[..., Any] | None = None,
    ) -> None:
        self._is_dynamic_expression = is_dynamic_expression
        self._loop_execute_range_dynamic = loop_execute_range_dynamic
        self._if_dynamic = if_dynamic
        self._while_dynamic = while_dynamic
        self._compare_executor = compare_executor
        self._builtin_redirector = builtin_redirector
        self._ifexp_dynamic = ifexp_dynamic

    @staticmethod
    def convert_to_list(x: Any) -> list[Any]:
        """This function is used to convert x to a list.
        If x is None, return an empty list.
        If x is not a list, return a list containing x.
        Otherwise, return x itself.
        """
        if x is None:
            return []
        if not isinstance(x, list):
            return [x]
        return x

    @staticmethod
    def converge_ret_val(res: Any) -> Any:
        """This function is used to converge res (the return value) of the function.
        If res is None, return None.
        If res is a list and has only one element, return the element.
        Otherwise, return res itself.
        """
        if res is None:
            return res
        elif isinstance(res, list) and len(res) == 1:
            return res[0]
        return res

    def for_execute(
        self,
        func: Callable[..., Any],
        start: Any,
        stop: Any,
        step: Any,
        write_args: list[Any] = [],
        full_write_args_count: int = 0,
        write_args_names: list[str] = [],
        unroll: int = -1,
        unroll_full: bool = False,
        prefetch_stages: int | None = None,
        vectorize: int | None = None,
        at_least_once: bool = False,
    ) -> Any:
        assert self._loop_execute_range_dynamic, (
            "Functions must be set before execution."
        )
        log().debug("start [%s] stop [%s] step [%s]", start, stop, step)

        return self._loop_execute_range_dynamic(
            func,
            start,
            stop,
            step,
            write_args=write_args,
            full_write_args_count=full_write_args_count,
            write_args_names=write_args_names,
            unroll=unroll,
            unroll_full=unroll_full,
            prefetch_stages=prefetch_stages,
            vectorize=vectorize,
            at_least_once=at_least_once,
        )

    def if_execute(
        self,
        pred: Any,
        then_block: Callable[..., Any],
        else_block: Callable[..., Any] | None = None,
        write_args: list[Any] = [],
        full_write_args_count: int = 0,
        write_args_names: list[str] = [],
    ) -> Any:
        assert self._if_dynamic, "Functions must be set before execution."

        # MLIR generation
        return self._if_dynamic(
            pred,
            then_block,
            else_block,
            write_args,
            full_write_args_count,
            write_args_names,
        )

    def while_execute(
        self,
        while_before_block: Callable[..., Any],
        while_after_block: Callable[..., Any],
        write_args: list[Any] = [],
        full_write_args_count: int = 0,
        write_args_names: list[str] = [],
    ) -> Any:
        assert self._while_dynamic, "Functions must be set before execution."

        # MLIR generation
        return self._while_dynamic(
            while_before_block,
            while_after_block,
            write_args,
            full_write_args_count,
            write_args_names,
        )

    def ifexp_execute(
        self,
        pred: Any,
        block_args: tuple[Any, ...],
        then_block: Callable[..., Any],
        else_block: Callable[..., Any],
    ) -> Any:
        assert self._ifexp_dynamic, "Functions must be set before execution."
        return self._ifexp_dynamic(pred, block_args, then_block, else_block)


